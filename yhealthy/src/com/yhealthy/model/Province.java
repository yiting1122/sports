package com.yhealthy.model;

public class Province {
	private String Id;
	private String name;
	
	public Province() {
	}
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
